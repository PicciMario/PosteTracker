    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String newStatusString = jTextStatus.getText();
        String newDateString = jTextData.getText();
        
        if (newStatusString.length() == 0 || newDateString.length() == 0){
            JOptionPane.showMessageDialog(this, "Status e data non possono essere lasciati in bianco.");
            return;
        }
        
        SimpleDateFormat dateParser = new SimpleDateFormat("dd/MM/yyyy");
        Date newDate;
        
        try{
            newDate = dateParser.parse(newDateString);
        } 
        catch (Exception e){
            JOptionPane.showMessageDialog(this, "La data inserita non Ã¨ valida, deve essere nel formato gg/mm/aaaa (esempio: 5/3/1983).");
            return;            
        }
        
        ProductStatus newStatus = new ProductStatus(newStatusString, newDate);
        newStatus.setManual(true);
        posteParent.addProductStatus(prod, newStatus);
        JOptionPane.showMessageDialog(this, "Nuovo status aggiunto correttamente.");
        
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

